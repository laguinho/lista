var backdrop = (function() {
	return {
		show: function() {
			$backdrop.addClass("in");
		},
		hide: function() {
			$backdrop.removeClass("in").off("hide");
		}
	};
})();

// jQuery
var $backdrop;

$(function() {
	$backdrop = $("#backdrop");
	$backdrop.on("click", function() {
		$backdrop.trigger("hide");
	});
});
